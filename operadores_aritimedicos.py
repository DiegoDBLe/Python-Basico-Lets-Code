# Variavel x e y recebem valores diferentes:
x = 50
y = 2

# Somando:
print(f'Somando: x + y = {x + y}')
print()
# Subtraindo:
print(f'Subtraindo: x - y = {x - y}')
print()
# Multiplicando:
print(f'Multiplicando: x * y = {x * y}')
print()
# Dividindo:
print(f'Dividindo: x / y = {x / y}')
print()
# Potência:
print(f'Potência: x ** y = {x ** y}')
print()
# Parte inteira da Divisão:
print(f'Parte inteira da Divisão: x // y = {x // y}')
print()
# Resto da Divisão:
print(f'Resto da Divisão: x % y = {x % y}')
print()
# Operadores Lógicos:
print('Operadores Lógicos:')
tem_cafe = True
tem_pao = False
print(f'Não tem café: {not tem_cafe}')
print(f'Tem café ou pão: {tem_cafe or tem_pao}')
print(f'Tem café e pão: {tem_cafe and tem_pao}')
print()
dolar = 5.6
real = 1
print(f'Dolar é maior que real: {dolar > real}')
print(f'Dolar é menor que real: {dolar < real}')
print(f'Dolar é igual ao real: {dolar == real}')
print(f'Dolar é maior ou igual o real: {dolar >= real}')
print(f'Dolar é menor ou igual o real: {dolar <= real}')
print(f'Dolar é diferente do real: {dolar != real}')





